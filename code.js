// Функция для создания сетки изображений
function createImageGrid() {
    const grid = document.getElementById('imageGrid');
    grid.innerHTML = ''; // Очищаем сетку перед созданием

    images.forEach((imgUrl, index) => {
        const item = document.createElement('div');
        item.className = 'grid-item';

        const img = new Image();
        img.src = imgUrl;
        img.alt = `Image ${index + 1}`;
        img.loading = 'lazy';
        img.classList.add('grid-img');

        const openBtn = document.createElement('a');
        openBtn.href = imgUrl;
        openBtn.target = '_blank';
        openBtn.className = 'download-btn';
        openBtn.textContent = 'DOWNLOAD';

        img.onload = function() {
            const ratio = this.naturalWidth / this.naturalHeight;
            
            // Удаляем предыдущие классы
            item.classList.remove('wide', 'tall');
            
            // Классифицируем изображения
            if (ratio > 1.4) {
                item.classList.add('wide');
            } else if (ratio < 0.8) {
                item.classList.add('tall');
            }
            
            // Добавляем элемент в сетку только после загрузки изображения
            item.appendChild(img);
            item.appendChild(openBtn);
            grid.appendChild(item);
        };

        // Обработка ошибок загрузки изображений
        img.onerror = function() {
            console.error('Failed to load image:', imgUrl);
        };
    });

    document.getElementById('imageCount').textContent = images.length;
}

// Инициализация сетки при загрузке и при изменении размера окна
window.addEventListener('DOMContentLoaded', createImageGrid);
window.addEventListener('resize', createImageGrid);

